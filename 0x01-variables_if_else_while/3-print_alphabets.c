#include <stdio.h>

/**
 * main - entry point
 *
 * Return: Always 0 (success)
 */

int main(void)
{
	int lowerCase = 'a';
	int upperCase = 'A';
	while (lowercase <= 'z')
	{
		putchar(lowerCase);
		lowerCase += 1;
	}
	while (upperCase <= 'z')
	{
		putchar (upperCase);
		upperCase += 1;
	}
	return (0);
}
